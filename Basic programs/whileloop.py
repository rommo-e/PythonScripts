x = 22 # We initialize the value for a certain variable in this case x

# Then we work with a condition that is going to iterate until it is false
# for example if we want to see how many times can we sum 22 until it is less than 220 
while x < 220:
    print("Another sum, x="+ str(x))
    x = x + 22
